#include "ft_tools.h"

void		ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n / 10 > 0)
		ft_putnbr(n / 10);
	ft_putchar('0' + n % 10);
}
